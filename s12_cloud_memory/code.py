def load_cloud_profile() -> str:
    """Load cloud profile from local cache.

    In real WorkBuddy:
    - Server sends profile at session start
    - Client caches it at ~/.workbuddy/memory/
    - Cache is READ-ONLY — server overwrites on next session
    - If you modify it locally, your changes are lost

    Teaching version: we seed it if not present, but mark it read-only.
    """
    if MEMORY_CACHE_FILE.exists():
        return MEMORY_CACHE_FILE.read_text()

    # Seed the cache (simulating server pushing profile at session start)
    MEMORY_CACHE_DIR.mkdir(parents=True, exist_ok=True)
    MEMORY_CACHE_FILE.write_text(CLOUD_PROFILE)
    try:
        MEMORY_CACHE_FILE.chmod(0o444)  # read-only
    except Exception:
        pass
    return CLOUD_PROFILE


