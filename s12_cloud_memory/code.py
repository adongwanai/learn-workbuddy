def recall_history(query: str, limit: int = 5) -> str:
    """Search through past conversations with server-side ranking.

    CRITICAL: This tool has ZERO access to the current conversation!
    The query MUST be self-contained — restate what you're looking for,
    what context you need, and why.

    Production harness: server-side vector + keyword hybrid search.
    Teaching version: keyword overlap + time decay scoring.
    """
    query_terms = set(_tokenize(query))
    if not query_terms:
        return "查询为空, 无法搜索。"

    scored = []
    for conv in CONVERSATION_DB:
        conv_terms = set(_tokenize(conv["content"] + " " + conv["summary"]))

        # Keyword overlap score
        overlap = len(query_terms & conv_terms)
        if overlap == 0:
            continue

        # Time decay: recent conversations score higher
        days_ago = (datetime.now() - conv["date"]).days
        time_weight = 1.0 / (1 + days_ago * 0.02)

        score = overlap * time_weight
        scored.append((score, conv))

    if not scored:
        return "(未找到相关历史对话)"

    scored.sort(key=lambda x: -x[0])

    results = []
    for score, conv in scored[:limit]:
        date_str = conv["date"].strftime("%Y-%m-%d")
        results.append(
            f"## 对话记录 ({date_str}) [相关度: {score:.2f}]\n"
            f"{conv['summary']}\n"
        )

    return "\n---\n".join(results)


def _tokenize(text: str) -> list[str]:
    """Simple tokenizer for keyword matching.

    Production systems often use tokenizer/embedding based retrieval.
    Teaching version: split on whitespace and punctuation, lowercase.
    """
    import re
    # Split on non-word characters (works for both Chinese and English)
    tokens = re.findall(r'[\w]+', text.lower())
    # Also split Chinese text into bi-grams for better matching
    chinese_chars = re.findall(r'[\u4e00-\u9fff]+', text)
    for seg in chinese_chars:
        for i in range(len(seg) - 1):
            tokens.append(seg[i:i+2])
    return tokens


